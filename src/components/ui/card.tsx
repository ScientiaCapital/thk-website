import * as React from 'react'
import { cn } from '@/lib/utils'

const Card = React.forwardRef<
  HTMLDivElement,
  React.HTMLAttributes<HTMLDivElement> & { gradient?: string }
>(({ className, gradient, children, ...props }, ref) => (
  <div
    ref={ref}
    className={cn(
      'relative rounded-2xl border border-blue-500/10 bg-navy-950 p-6 transition-all duration-300',
      'hover:border-blue-500/20 hover:-translate-y-1 hover:shadow-thk-lg',
      'overflow-hidden',
      className
    )}
    {...props}
  >
    {gradient && (
      <div className={cn('absolute inset-x-0 top-0 h-[3px]', gradient)} />
    )}
    {children}
  </div>
))
Card.displayName = 'Card'

